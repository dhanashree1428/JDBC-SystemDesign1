package com.product.dto;

public class Customer {
private int id;
private String name;
private long cno;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public long getCno() {
	return cno;
}
public void setCno(long cno) {
	this.cno = cno;
}
}
